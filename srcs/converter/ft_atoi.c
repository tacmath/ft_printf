int	ft_atoi(const char *str)
{
	int resultat;
	int n;

	resultat = 0;
	n = 0;
	while (str[n] >= '0' && str[n] <= '9')
	{
		resultat = resultat * 10 + (str[n] - 48);
		n++;
	}
	return (resultat);
}
